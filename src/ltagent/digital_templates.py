# ---------------------------------------------------------------------------
# Mapping of file name -> (template body, IR-driven substitutions)
# ---------------------------------------------------------------------------

# The generator looks up a template by its relative path. The
# ``ir_substitutions`` callable receives the validated DesignIR
# and returns a dict used for ``str.format`` substitution. Keeping
# the substitution as a function (not a constant dict) means the
# IR can drive file content beyond simple width fields.

# Substitutions the generator always provides:
#   - ``header``: the do-not-edit banner.
#   - everything the IR-driven ``ir_subs`` adds.
