TINY8_CPU_V: str = """\
__HEADER__module tiny8_cpu #(
    parameter ADDR_WIDTH   = 8,
    parameter DATA_WIDTH   = 8,
    parameter INSTR_WIDTH  = 16
) (
    input  wire                    clk,
    input  wire                    rst,

    // External memory busses. The wrapper (tiny8_top) wires these
    // to the ROM and RAM instances.
    output wire [ADDR_WIDTH-1:0]   rom_addr,
    input  wire [INSTR_WIDTH-1:0]  rom_data,

    output wire                    ram_we,
    output wire [ADDR_WIDTH-1:0]   ram_addr,
    output wire [DATA_WIDTH-1:0]   ram_wdata,
    input  wire [DATA_WIDTH-1:0]   ram_rdata,

    // IO ports (v1: just an OUT strobe + data)
    output wire                    out_valid,
    output wire [DATA_WIDTH-1:0]   out_port,
    output wire [DATA_WIDTH-1:0]   out_data,

    // Status visible to the wrapper and the testbench.
    output wire [ADDR_WIDTH-1:0]   pc_out,
    output wire [DATA_WIDTH-1:0]   acc_out,
    output wire                    halted_out
);
    // Program counter.
    reg [ADDR_WIDTH-1:0] pc;
    reg [ADDR_WIDTH-1:0] next_pc;

    // Accumulator + zero flag.
    reg [DATA_WIDTH-1:0] acc;
    reg                  zero_flag;

    // Sticky halt.
    reg                  halted;

    // Current instruction, captured at fetch.
    reg [INSTR_WIDTH-1:0] instr;

    // ALU + control.
    wire [10:0] ctl;
    wire        illegal;
    wire [7:0]  alu_result;
    wire        alu_zero;

    tiny8_control u_control (
        .instr     (instr),
        .zero_flag (zero_flag),
        .ctl       (ctl),
        .illegal   (illegal)
    );

    tiny8_alu u_alu (
        .op      (ctl[5:2]),
        .acc     (acc),
        .operand (ram_rdata),
        .result  (alu_result),
        .zero    (alu_zero)
    );

    // pc update
    always @* begin
        if (ctl[9])
            next_pc = instr[ADDR_WIDTH-1:0];
        else if (ctl[8])
            next_pc = pc + 1'b1;
        else
            next_pc = pc;
    end

    // main state register
    always @(posedge clk) begin
        if (rst) begin
            pc        <= {ADDR_WIDTH{1'b0}};
            acc       <= {DATA_WIDTH{1'b0}};
            zero_flag <= 1'b1;
            halted    <= 1'b0;
            instr     <= {INSTR_WIDTH{1'b0}};
        end else if (!halted) begin
            // Fetch
            instr <= rom_data;
            // pc
            pc    <= next_pc;
            // acc
            if (ctl[7])
                acc <= alu_result;
            // zero_flag updates when acc is written or after ALU pass.
            if (ctl[7])
                zero_flag <= alu_zero;
            // halt
            if (ctl[10])
                halted <= 1'b1;
        end
        // Illegal opcode: leave state alone for visibility; the
        // testbench checks ``illegal`` and fails the run.
    end

    assign rom_addr = pc;
    assign ram_we   = ctl[6];
    assign ram_addr = instr[ADDR_WIDTH-1:0];
    assign ram_wdata= acc;
    assign out_valid= ctl[1];
    assign out_port = instr[DATA_WIDTH-1:0];
    assign out_data = acc;
    assign pc_out   = pc;
    assign acc_out  = acc;
    assign halted_out = halted;
endmodule
"""


