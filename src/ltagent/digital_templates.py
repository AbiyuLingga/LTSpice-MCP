# ---------------------------------------------------------------------------
# tiny8_alu
# ---------------------------------------------------------------------------

# A 4-bit ALU opcode is internal to this module. The 16-bit ISA
# opcode is decoded by ``tiny8_control`` into these internal
# signals. Keeping a tiny private opcode space here makes the
# ALU self-contained and easy to test in isolation.
#
# Local ALU opcodes (must match ``tiny8_control``):
#   0 = PASS (operand -> acc, no arithmetic)
#   1 = ADD
#   2 = SUB
#   3 = AND
#   4 = OR
#   5 = XOR

ALU_OP_PASS: int = 0
ALU_OP_ADD: int = 1
ALU_OP_SUB: int = 2
ALU_OP_AND: int = 3
ALU_OP_OR: int = 4
ALU_OP_XOR: int = 5

TINY8_ALU_V: str = """\
__HEADER__module tiny8_alu (
    input  wire [3:0] op,
    input  wire [7:0] acc,
    input  wire [7:0] operand,
    output reg  [7:0] result,
    output wire       zero
);
    // Combinational: select operation, compute result, drive zero flag.
    always @* begin
        case (op)
            4'd0: result = acc;            // PASS (LDI / LDA / STA / OUT / IN)
            4'd1: result = acc + operand;  // ADD
            4'd2: result = acc - operand;  // SUB
            4'd3: result = acc & operand;  // AND
            4'd4: result = acc | operand;  // OR
            4'd5: result = acc ^ operand;  // XOR
            default: result = 8'h00;
        endcase
    end

    assign zero = (result == 8'h00);
endmodule
"""


# ---------------------------------------------------------------------------
# tiny8_control
# ---------------------------------------------------------------------------

# Internal control signals driven by the instruction opcode. The
# ALU op is a 4-bit one-hot-ish field; the rest are 1-bit enables.
#
# control_out layout (output reg [10:0]):
#   [10]   halted_next (sticky once set)
#   [9]    pc_load    (1 = pc <= operand for JMP/JZ/JNZ taken)
#   [8]    pc_inc     (1 = pc <= pc + 1, default)
#   [7]    acc_we     (1 = write acc from alu result)
#   [6]    ram_we     (1 = write ram[operand] = acc)
#   [5:2]  alu_op     (4-bit ALU opcode; PASS=0, ADD=1, ...)
#   [1]    out_valid  (1 = drive out_port/out_data this cycle)
#   [0]    ram_re     (1 = operand fed to ram addr this cycle)

TINY8_CONTROL_V: str = """\
__HEADER__module tiny8_control (
    input  wire [15:0] instr,
    input  wire        zero_flag,
    output reg  [10:0] ctl,
    output reg         illegal
);
    // Decode the 4-bit ISA opcode and the 8-bit operand.
    wire [3:0] opcode = instr[15:12];
    wire [7:0] operand = instr[7:0];

    // Default: pc++ and no side-effects.
    always @* begin
        ctl     = 11'b0_0_0_0_0000_0_0;
        illegal = 1'b0;
        ctl[8]  = 1'b1; // pc_inc by default

        case (opcode)
            4'h0: begin // NOP
                // nothing extra
            end
            4'h1: begin // LDI imm
                ctl[7]    = 1'b1; // acc_we
                ctl[5:2]  = 4'd0; // ALU PASS
            end
            4'h2: begin // LDA addr
                ctl[7]    = 1'b1; // acc_we
                ctl[0]    = 1'b1; // ram_re
                ctl[5:2]  = 4'd0; // ALU PASS
            end
            4'h3: begin // STA addr
                ctl[6]    = 1'b1; // ram_we
            end
            4'h4: begin // ADD addr
                ctl[7]    = 1'b1;
                ctl[0]    = 1'b1;
                ctl[5:2]  = 4'd1; // ALU ADD
            end
            4'h5: begin // SUB addr
                ctl[7]    = 1'b1;
                ctl[0]    = 1'b1;
                ctl[5:2]  = 4'd2; // ALU SUB
            end
            4'h6: begin // AND addr
                ctl[7]    = 1'b1;
                ctl[0]    = 1'b1;
                ctl[5:2]  = 4'd3; // ALU AND
            end
            4'h7: begin // OR addr
                ctl[7]    = 1'b1;
                ctl[0]    = 1'b1;
                ctl[5:2]  = 4'd4; // ALU OR
            end
            4'h8: begin // XOR addr
                ctl[7]    = 1'b1;
                ctl[0]    = 1'b1;
                ctl[5:2]  = 4'd5; // ALU XOR
            end
            4'h9: begin // JMP addr
                ctl[9]    = 1'b1; // pc_load
                ctl[8]    = 1'b0; // override pc_inc
            end
            4'hA: begin // JZ addr
                if (zero_flag) begin
                    ctl[9] = 1'b1; // pc_load
                    ctl[8] = 1'b0;
                end
            end
            4'hB: begin // JNZ addr
                if (!zero_flag) begin
                    ctl[9] = 1'b1;
                    ctl[8] = 1'b0;
                end
            end
            4'hC: begin // OUT port
                ctl[1]    = 1'b1; // out_valid
            end
            4'hD: begin // IN port
                ctl[7]    = 1'b1; // acc_we (from in_data; tiny8_cpu wires this)
                ctl[5:2]  = 4'd0; // ALU PASS
            end
            4'hE: begin // reserved
                illegal   = 1'b1;
            end
            4'hF: begin // HALT
                ctl[10]   = 1'b1; // halted_next
                ctl[8]    = 1'b0; // do not advance pc
            end
            default: begin
                illegal   = 1'b1;
            end
        endcase
    end
endmodule
"""


