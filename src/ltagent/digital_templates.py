"""Tiny8 Verilog-2001 templates (Phase 12, v1).

The generator (``ltagent.digital_generator``) composes these
templates with the ``DesignIR`` to produce the project's RTL
files. **No free-form HDL is ever produced.** Every Verilog
character on disk came from one of these strings + a validated
``DesignIR`` field.

Style conventions (carried from docs/digital/plan-tiny8-agent.md
§8.1 and the lowRISC Verilog style guide):

* Verilog-2001 only. No SystemVerilog.
* Module names: ``tiny8_<block>``.
* Signal names: descriptive ``snake_case``.
* Clock/reset ports first: ``clk``, ``rst``.
* Sequential logic: nonblocking assignment.
* Combinational logic: ``always @*`` with blocking.
* No inferred latches, no unsized constants in critical datapath.
* Every file has the do-not-edit header at the top.

Each module is exposed as a Python string constant. The
generator uses ``str.format()`` (with explicit ``{...}`` keys
for the IR-driven values) to render the file. **No
``str.format()`` is used on the body of the Verilog itself**;
the templates use sentinel markers (``%(name)s``-style would be
insecure; we use plain ``str.replace`` on stable tokens in the
generator).
"""

