# ---------------------------------------------------------------------------
# tb_tiny8_top
# ---------------------------------------------------------------------------

# The testbench is driven by the ``program.source`` assembler
# output. It loads the .mem at time 0, asserts reset, waits for
# halt, then checks the verification contract.
#
# The testbench expects the following to be substituted by the
# generator:
#   ROM_FILE        path to the .mem file (e.g. "demo.mem")
#   MAX_CYCLES      expectedHaltCyclesMax (decimal integer)
#   EXP_HALTED      "1'b1" (always true for v1)
#   EXP_ACC         "8'h2a" (default: 42 = 0x2a)
#   EXP_MEM_CHECKS  list of "if (ram[16] !== 8'h14) $fatal(1); ..." etc.

TINY8_TB_V: str = """\
__HEADER__`timescale 1ns/1ps

module tb_tiny8_top;
    reg         clk;
    reg         rst;
    wire        out_valid;
    wire [7:0]  out_port;
    wire [7:0]  out_data;
    wire [7:0]  pc;
    wire [7:0]  acc;
    wire        halted;

    // Expose the DUT's RAM for direct verification.
    reg [7:0]  ram_mem [0:255];

    integer    cycles;
    reg        failed;

    tiny8_top u_dut (
        .clk       (clk),
        .rst       (rst),
        .out_valid (out_valid),
        .out_port  (out_port),
        .out_data  (out_data),
        .pc_out    (pc),
        .acc_out   (acc),
        .halted_out(halted)
    );

    // 100 MHz-ish clock: 10 ns period.
    initial clk = 1'b0;
    always #5 clk = ~clk;

    // Capture RAM writes for verification. The DUT's RAM is internal
    // to tiny8_top, so we mirror the same protocol from a testbench
    // point of view by snooping the bus here.
    //
    // The testbench drives the same write events by peeking at the
    // CPU's bus through a hierarchical reference. Icarus and Verilator
    // both support this.
    always @(posedge clk) begin
        if (!rst && u_dut.u_cpu.ram_we) begin
            ram_mem[u_dut.u_cpu.ram_addr] <= u_dut.u_cpu.ram_wdata;
        end
    end

    initial begin
        failed = 1'b0;
        // reset
        rst    = 1'b1;
        cycles = 0;
        ram_mem = '{256{8'h00}};
        #20;
        rst = 1'b0;
    end

    // cycle counter + halt timeout
    always @(posedge clk) begin
        if (!rst) begin
            cycles <= cycles + 1;
            if (halted) begin
                $display("TB_PASS halted at cycle %0d acc=%h", cycles, acc);
                // Verification contract
                if (__EXP_HALTED__) begin
                    $display("TB_CHECK halted=1 ok");
                end else begin
                    $display("TB_FAIL halted mismatch");
                    failed = 1'b1;
                end
                if (__EXP_ACC_EN__) begin
                    if (acc !== __EXP_ACC__) begin
                        $display("TB_FAIL acc expected __EXP_ACC__ got %h", acc);
                        failed = 1'b1;
                    end else begin
                        $display("TB_CHECK acc=__EXP_ACC__ ok");
                    end
                end
__EXP_MEM_CHECKS__
                if (failed) begin
                    $display("TB_FAIL overall");
                    $fatal(1);
                end else begin
                    $display("TB_PASS overall");
                    $finish;
                end
            end
            if (cycles > __MAX_CYCLES__) begin
                $display("TB_FAIL timeout after %0d cycles (halted=%b acc=%h)",
                         cycles, halted, acc);
                $fatal(1);
            end
        end
    end
endmodule
"""


