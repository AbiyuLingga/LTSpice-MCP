"""Unit tests for ``ltagent.netlist`` (Phase 2).

These tests cover the acceptance criteria from plan section 21 (Phase 2):

- Voltage divider and RC examples generate valid-looking ``.cir`` files.
- Snapshot tests check required lines (header, components, analyses,
  measurements, ``.end``).
- Unsafe directives are rejected with a structured error code.
- The IR's ``.end`` is not emitted as a duplicate.

Tests do not require LTspice, Wine, or the network.
"""

