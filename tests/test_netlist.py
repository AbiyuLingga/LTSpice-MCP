# --- directive safety ----------------------------------------------------


def test_include_directive_is_rejected_by_default() -> None:
    ir = load_ir(EXAMPLES_DIR / "rc_lowpass.ir.json")
    ir.directives.append(".include /etc/passwd")
    with pytest.raises(NetlistError) as excinfo:
        render_netlist(ir)
    assert excinfo.value.code == "DIR_UNSUPPORTED"
    assert ".include" in str(excinfo.value)
    assert "/etc/passwd" not in str(excinfo.value) or "directive" in str(excinfo.value)


def test_lib_directive_is_rejected_by_default() -> None:
    ir = load_ir(EXAMPLES_DIR / "rc_lowpass.ir.json")
    ir.directives.append(".lib /models/foo.lib")
    with pytest.raises(NetlistError) as excinfo:
        render_netlist(ir)
    assert excinfo.value.code == "DIR_UNSUPPORTED"
    assert ".lib" in str(excinfo.value)


def test_end_directive_is_never_in_directives_allowlist() -> None:
    """``.end`` belongs to the file structure, not the IR. The IR's
    allowlist must not include it, and the netlist generator appends
    its own ``.end`` exactly once.
    """
    from ltagent.ir import DIRECTIVE_ALLOWLIST as IR_ALLOWLIST
    from ltagent.netlist import DIRECTIVE_ALLOWLIST as NL_ALLOWLIST

    assert ".end" not in IR_ALLOWLIST
    assert ".end" not in NL_ALLOWLIST

    # Standard example: no .end duplication, exactly one .end at the end.
    ir = load_ir(EXAMPLES_DIR / "rc_lowpass.ir.json")
    text = render_netlist(ir).text
    assert text.count(".end") == 1
    assert text.rstrip().endswith(".end")


def test_allowed_directive_passes_through() -> None:
    ir = load_ir(EXAMPLES_DIR / "rc_lowpass.ir.json")
    ir.directives.append(".options reltol=0.001")
    text = render_netlist(ir).text
    assert ".options reltol=0.001" in text


def test_allow_unknown_directives_drops_rejected() -> None:
    ir = load_ir(EXAMPLES_DIR / "rc_lowpass.ir.json")
    ir.directives.append(".include /etc/passwd")
    ir.directives.append(".options reltol=0.001")
    result = render_netlist(ir, allow_unknown_directives=True)
    assert result.rejected_directives == [".include /etc/passwd"]
    assert ".include" not in result.text
    assert ".options reltol=0.001" in result.text


def test_allowlist_matches_doc_string() -> None:
    """Sanity: the allowlist must be a non-empty frozenset of strings
    starting with a dot."""
    assert isinstance(DIRECTIVE_ALLOWLIST, frozenset)
    assert DIRECTIVE_ALLOWLIST
    for entry in DIRECTIVE_ALLOWLIST:
        assert entry.startswith(".")
    # Path-bearing directives must be excluded (plan section 18.1).
    assert ".include" not in DIRECTIVE_ALLOWLIST
    assert ".lib" not in DIRECTIVE_ALLOWLIST


