# --- helpers --------------------------------------------------------------


def _strip_blank_lines(text: str) -> list[str]:
    return [line for line in text.splitlines() if line.strip()]


def _render_lines(ir: CircuitIR) -> list[str]:
    return _strip_blank_lines(render_netlist(ir).text)


# --- example snapshot tests ----------------------------------------------


@pytest.mark.parametrize("example_name", EXAMPLES)
def test_example_netlist_has_required_sections(example_name: str) -> None:
    """All three MVP examples must produce a netlist with header,
    components, at least one analysis, optional measurements, and ``.end``."""
    ir = load_ir(EXAMPLES_DIR / f"{example_name}.ir.json")
    result = render_netlist(ir)
    lines = _render_lines(ir)

    # Header.
    assert lines[0].startswith(f"* Generated by {GENERATOR_NAME}")
    assert f"{__version__}" in lines[0]
    assert f"* Project: {ir.name}" in lines[1]
    assert f"* Topology: {ir.topology}" in lines[2]

    # Component lines (one per IR component, in IR order). A component
    # line starts with an identifier (letter, not `*` or `.`) followed by
    # nodes and a value.
    component_line_ids = [
        line.split()[0]
        for line in lines
        if line and not line.startswith(".") and not line.startswith("*")
    ]
    assert component_line_ids == [c.id for c in ir.components]

    # .end at the end.
    assert lines[-1] == ".end"
    # .end must appear exactly once.
    assert sum(1 for line in lines if line.strip() == ".end") == 1

    # At least one analysis directive.
    analysis_lines = [line for line in lines if line.startswith(".")]
    assert any(line.startswith((".tran", ".op", ".dc", ".ac")) for line in analysis_lines)
    assert result.analysis_count >= 1

    # Counters.
    assert result.component_count == len(ir.components)
    assert result.measurement_count == len(ir.measurements)


def test_voltage_divider_netlist_matches_plan_reference() -> None:
    """Plan section 11.2 lists the RC low-pass output. Voltage divider is
    also in the MVP and must follow the same shape."""
    ir = load_ir(EXAMPLES_DIR / "voltage_divider.ir.json")
    text = render_netlist(ir).text
    lines = _strip_blank_lines(text)
    assert lines[0] == f"* Generated by {GENERATOR_NAME} {__version__}"
    assert lines[1] == "* Project: voltage_divider_12v_to_5v"
    assert lines[2] == "* Topology: voltage_divider"
    assert "Vin in 0 DC 12" in lines
    assert "R1 in out 1.4k" in lines
    assert "R2 out 0 1k" in lines
    assert ".op" in lines
    assert ".meas op VOUT V(out)" in lines
    assert lines[-1] == ".end"


def test_rc_lowpass_netlist_matches_plan_reference() -> None:
    """Plan section 11.2 publishes the exact RC low-pass netlist shape."""
    ir = load_ir(EXAMPLES_DIR / "rc_lowpass.ir.json")
    text = render_netlist(ir).text
    lines = _strip_blank_lines(text)
    assert lines[0] == f"* Generated by {GENERATOR_NAME} {__version__}"
    assert lines[1] == "* Project: rc_lowpass_1khz"
    assert lines[2] == "* Topology: rc_lowpass"
    # Component order must match IR order.
    assert "Vin in 0 SINE(0 1 1k)" in lines
    assert "R1 in out 1.59k" in lines
    assert "C1 out 0 100n" in lines
    # ngspice requires TSTEP > 0; default to 1/1000 of TSTOP.
    assert ".tran 5e-06 5m" in lines
    assert ".meas tran VOUT_MAX MAX V(out)" in lines
    assert ".meas tran VOUT_MIN MIN V(out)" in lines
    assert lines[-1] == ".end"


