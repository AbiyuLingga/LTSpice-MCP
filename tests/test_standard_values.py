"""Tests for :mod:`ltagent.math_core.standard_values`.

The standard-values module is the math core's preferred-value lookup
table. The tests pin down the IEC 60063 series sizes and the
nearest-value selection algorithm so a refactor cannot accidentally
loosen the tolerance.
"""

from __future__ import annotations

import math

import pytest

from ltagent.math_core.standard_values import (
    SUPPORTED_SERIES,
    StandardValueSelection,
    calculate_error_percent,
    decade_of,
    nearest_standard_value,
    series_values,
)

# ---------------------------------------------------------------------------
# Series tables
# ---------------------------------------------------------------------------


def test_supported_series_includes_e6_e12_e24() -> None:
    """The MVP series set is non-negotiable per the live-editing plan."""
    assert {"E6", "E12", "E24"} <= SUPPORTED_SERIES


def test_e6_has_six_values_per_decade() -> None:
    ladder = series_values("E6")
    decade_values = [v for v in ladder if 1 <= v < 10]
    assert len(decade_values) == 6
    # The classic E6 mantissas.
    expected = {1.0, 1.5, 2.2, 3.3, 4.7, 6.8}
    assert {round(v, 6) for v in decade_values} == expected


def test_e12_has_twelve_values_per_decade() -> None:
    ladder = series_values("E12")
    decade_values = [v for v in ladder if 1 <= v < 10]
    assert len(decade_values) == 12
    expected = {1.0, 1.2, 1.5, 1.8, 2.2, 2.7, 3.3, 3.9, 4.7, 5.6, 6.8, 8.2}
    assert {round(v, 6) for v in decade_values} == expected


def test_e24_has_twenty_four_values_per_decade() -> None:
    ladder = series_values("E24")
    decade_values = [v for v in ladder if 1 <= v < 10]
    assert len(decade_values) == 24
    expected = {
        1.0, 1.1, 1.2, 1.3, 1.5, 1.6, 1.8, 2.0, 2.2, 2.4, 2.7, 3.0,
        3.3, 3.6, 3.9, 4.3, 4.7, 5.1, 5.6, 6.2, 6.8, 7.5, 8.2, 9.1,
    }
    assert {round(v, 6) for v in decade_values} == expected


def test_series_values_spans_a_wide_range() -> None:
    """The ladder covers at least 1e-12 .. 1e12 so any engineering
    value can be matched without overflow."""
    ladder = series_values("E24")
    assert min(ladder) <= 1e-12
    assert max(ladder) >= 1e12


def test_series_values_are_sorted() -> None:
    ladder = series_values("E24")
    assert ladder == tuple(sorted(ladder))


def test_series_values_deduplicates() -> None:
    """E48 and E96 are generated by exponentiation; duplicates must
    be removed (e.g. 1.0 repeats across the integer-decade boundary)."""
    ladder = series_values("E96")
    assert len(ladder) == len(set(ladder))


def test_unsupported_series_raises() -> None:
    with pytest.raises(ValueError, match="unsupported series"):
        series_values("E1")


# ---------------------------------------------------------------------------
# calculate_error_percent
# ---------------------------------------------------------------------------


def test_calculate_error_percent_zero_ideal() -> None:
    """A zero ideal is handled gracefully (no division by zero)."""
    assert calculate_error_percent(0, 0) == 0.0
    # Non-zero selected is reported as percent of one (i.e. magnitude % 1).
    assert calculate_error_percent(0, 1) == 100.0
    assert calculate_error_percent(0, -1) == 100.0


def test_calculate_error_percent_signs() -> None:
    """``(selected - ideal) / ideal * 100`` — positive when selected
    is larger than the ideal."""
    assert calculate_error_percent(100, 110) == pytest.approx(10.0)
    assert calculate_error_percent(100, 90) == pytest.approx(-10.0)


def test_calculate_error_percent_rejects_nan() -> None:
    with pytest.raises(ValueError, match="finite"):
        calculate_error_percent(math.nan, 100.0)
    with pytest.raises(ValueError, match="finite"):
        calculate_error_percent(100.0, math.inf)


def test_calculate_error_percent_rejects_none() -> None:
    with pytest.raises(ValueError, match="finite"):
        calculate_error_percent(None, 100.0)  # type: ignore[arg-type]


