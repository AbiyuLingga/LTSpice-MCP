def test_nearest_standard_value_e24_picks_1p6k_for_1591() -> None:
    """Spec: 1kHz RC low-pass → ideal 1591.55 Ω → E24 picks 1.6kΩ."""
    sel = nearest_standard_value(1_591.55, "E24")
    assert isinstance(sel, StandardValueSelection)
    assert sel.series == "E24"
    assert sel.selected == pytest.approx(1_600.0)
    assert sel.error_percent == pytest.approx(0.528, rel=1e-2)


def test_nearest_standard_value_e12_picks_1p5k_for_1591() -> None:
    """E12's nearest is 1.5kΩ (no 1.6kΩ in the E12 ladder)."""
    sel = nearest_standard_value(1_591.55, "E12")
    assert sel.selected == pytest.approx(1_500.0)
    # Signed error: (1500 - 1591.55) / 1591.55 * 100 ≈ -5.7 %
    assert sel.error_percent == pytest.approx(-5.74, rel=1e-2)


def test_nearest_standard_value_e6_picks_1p5k_for_1591() -> None:
    """E6 has only 1.5 / 2.2 → 1.5k is closest."""
    sel = nearest_standard_value(1_591.55, "E6")
    assert sel.selected == pytest.approx(1_500.0)


def test_nearest_standard_value_exact_match() -> None:
    """A value that already exists in the series is returned unchanged."""
    sel = nearest_standard_value(1_000.0, "E24")
    assert sel.selected == 1_000.0
    assert sel.error_percent == 0.0


def test_nearest_standard_value_tie_breaks_larger() -> None:
    """A 50 Ω ideal sits exactly between 47 Ω and 51 Ω in E24. The
    tie-break must pick the *larger* value (safer default)."""
    sel = nearest_standard_value(50.0, "E24")
    # 47 and 51 are equidistant in percent terms; expect 51.
    assert sel.selected >= 50.0


def test_nearest_standard_value_handles_very_small() -> None:
    sel = nearest_standard_value(1e-9, "E24")
    assert sel.selected > 0
    assert sel.selected <= 1e-9 * 1.5


def test_nearest_standard_value_handles_very_large() -> None:
    sel = nearest_standard_value(1e9, "E24")
    assert sel.selected >= 1e9
    assert sel.selected <= 1e9 * 1.5


def test_nearest_standard_value_rejects_zero() -> None:
    with pytest.raises(ValueError, match="positive"):
        nearest_standard_value(0.0, "E24")


def test_nearest_standard_value_rejects_negative() -> None:
    with pytest.raises(ValueError, match="positive"):
        nearest_standard_value(-1_000.0, "E24")


def test_nearest_standard_value_rejects_unknown_series() -> None:
    with pytest.raises(ValueError, match="unsupported series"):
        nearest_standard_value(1_000.0, "E1")


def test_standard_value_selection_to_dict() -> None:
    sel = nearest_standard_value(1_591.55, "E24")
    payload = sel.to_dict()
    assert payload["ideal"] == pytest.approx(1_591.55)
    assert payload["series"] == "E24"
    assert payload["selected"] == pytest.approx(1_600.0)
    assert "errorPercent" in payload


