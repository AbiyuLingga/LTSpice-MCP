def test_decade_of_basic() -> None:
    assert decade_of(1.0) == 0
    assert decade_of(9.99) == 0
    assert decade_of(10.0) == 1
    assert decade_of(1_591.55) == 3
    assert decade_of(1e6) == 6
    assert decade_of(1e-9) == -9


def test_decade_of_rejects_non_positive() -> None:
    with pytest.raises(ValueError, match="positive"):
        decade_of(0.0)
    with pytest.raises(ValueError, match="positive"):
        decade_of(-1.0)
